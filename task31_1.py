n = int(input())
for i in range(n):
    if i == n - 1:
        print("(\___/)")
        break
    print("(\___/) ",end="")

for i in range(n):
    if i == n - 1:
        print("(='.'=)")
        break
    print("(='.'=) ",end="")

for i in range(n):
    if i == n - 1:
        print('(")_(")')
        break
    print('(")_(") ',end="")

